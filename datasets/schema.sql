CREATE DATABASE dimgame;

CREATE TABLE Games (
    AppID int,
    AppName varchar(255),
    ReleaseDateID int,
    SalesID int,
    PlatformsID int,
    TagsID int,
    PlaytimeID int,
    ReviewsID int,
    PRIMARY KEY (AppID),
    FOREIGN KEY (ReleaseDateID) REFERENCES ReleaseDate(ReleaseDateID),
    FOREIGN KEY (SalesID) REFERENCES Sales(SalesID),
    FOREIGN KEY (PlatformsID) REFERENCES Platforms(PlatformsID),
    FOREIGN KEY (TagsID) REFERENCES Tags(TagsID),
    FOREIGN KEY (PlaytimeID) REFERENCES Playtime(PlaytimeID),
    FOREIGN KEY (ReviewsID) REFERENCES Reviews(ReviewsID)
);

CREATE TABLE ReleaseDate (
    ReleaseDateID int,
    ReleaseYear int, -- check
    ReleaseMonth int, -- check
    ReleaseDay int, -- check
    PRIMARY KEY (ReleaseDateID)
);

CREATE TABLE Sales(
    SalesID int,
    Launch_Price float,
    Discounted_Price float,
    Estimated_Owners int, -- check
    PRIMARY KEY (SalesID)
);

CREATE TABLE Platforms(
    PlatformsID int,
    Windows tinyint,
    Linux tinyint,
    Mac tinyint,
    PRIMARY KEY (PlatformsID)
);

CREATE TABLE Reviews(
    ReviewsID int,
    Metacritic_Score int,
    User_Score int,
    Positive int,
    Negative int,
    PRIMARY KEY (ReviewsID)
);

CREATE TABLE Playtime (
    PlaytimeID int,
    Peak_CCU int,
    AveragePlaytimeForever float,
    AveragePlaytimeTwoWeeks float,
    MedianPlaytimeForever float,
    MedianPlaytimeTwoWeeks float,
    PRIMARY KEY (PlaytimeID)
);

